      <h6>Catatan:</h6>
      <figcaption class="figure-caption font-weight-light">
      <p class="text-justify">Cara untuk mengkonversikan bilangan oktal ke hexadesimal yaitu dengan mengkonversikan bilangan oktal tersebut ke biner terlebih dahulu kemudian bilangan biner tersebut dikonversikan ke bentuk hexadesimal.</p>
      <span class="font-weight-normal">Contoh:</span>
      <p>357<sub>8</sub> = ... <sub>16</sub></p>
       <ol class="mylist">
        <li>Kita pisahkan 357 menjadi  3, 5 dan 7 kemudian konversikan ke biner.</li>
         <li> 3  =  011       5  =  101        7  =  111</li>
          <li>Setelah dapat biner nya yaitu 0 1110 1111, kemudian konversi biner tersebut ke hexadesimal.</li>
          <li> 0 = 0 1110 = 14 = <strong>E</strong> 111 = 15 = <strong>F</strong></li>
        </ol>
      <p>Jadi, 357<sub>8</sub> = EF<sub>16</sub></p> 
      </figcaption>
