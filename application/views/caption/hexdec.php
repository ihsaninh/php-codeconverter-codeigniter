      <h6>Catatan:</h6>
      <figcaption class="figure-caption font-weight-light">
        <p>Cara untuk mengkonversikan bilangan hexadesimal ke desimal yaitu dengan menjumlahkan hasil kali setiap bilangan hexadesimal tersebut dengan 16 pangkat 0, 1, 2, 3, dst sesuai dengan deretan angka hexadesimal tersebut dari sebelah kanan. </p>
        <span class="font-weight-normal">Contoh:</span>
        <p>35FC<sub>16</sub> = ... <sub>10</sub></p>
        <p>Adapun cara pengerjaannya adalah seperti berikut ini:</p>
        <p>
          C  x  16<sup>0</sup> = 12 x 1 &nbsp;= 12<br>
          F  x  16<sup>1</sup> = 15 x 16 = 240<br>
          5  x  16<sup>2</sup> = 5 x 256 =  1280<br>
          3  x  16<sup>3</sup> = 3 x 512 =  12288   
        </p>
        <p>Lalu kita jumlahkan hasil kali tersebut, 12288 + 1280 + 240 + 12 = 13820</p>
        <p>Jadi, 35CF<sub>16</sub> = 13820<sub>10</sub></p> 
