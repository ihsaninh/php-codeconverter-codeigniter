        <span>Cara melakukan konversi:</span>
        <p>Contoh: 100011101<sub>2</sub> = ... <sub>8</sub></p> 
        <p>Pertama bagi-bagi dulu bilangan biner tersebut menjadi masing-masing 3 bit dari kanan ke kiri.</p>
        <p>Kelompok biner 1 : 101<br>
         Kelompok biner 2 : 011<br>
         Kelompok biner 3 : 100</p>
        <p>kemudian konversi kelompok-kelompok biner tersebut ke dalam bentuk desimal dimulai dari kelompok yang paling kiri sehingga menjadi :</p>
        <p>
        100 –> 4<br>
        011 –> 3<br>
        101 –> 5
        </p>
      <p>Jadi, 100011101<sub>2</sub> = 435<sub>8</sub></p> 
