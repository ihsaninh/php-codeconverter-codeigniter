      <h6>Catatan:</h6>
      <figcaption class="figure-caption font-weight-light">
      <p>Cara untuk mengkonversikan bilangan hexadesimal ke biner yaitu dengan memecah satu persatu bilangan-bilangan hexadesimal tersebut kemudian dikonversikan ke bilangan biner 4 digit.</p>
      <p>Contoh: F67<sub>16</sub> = ... <sub>2</sub></p> 
      <p>Kita pisahkan F67 menjadi F, 6 dan 7, kemudian kita konversi satu persatu ke biner 4 digit.</p>
      <p>F = 1111<br>
         6 = 0110<br>
         7 = 0111</p>
      <p>Jadi, F67<sub>16</sub> = 111101100111<sub>2</sub></p> 
