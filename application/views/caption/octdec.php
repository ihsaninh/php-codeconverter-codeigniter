      <h6>Catatan:</h6>
      <figcaption class="figure-caption font-weight-light">
      <p>Cara untuk mengkonversikan bilangan oktal ke desimal yaitu dengan menjumlahkan hasil kali setiap bilangan oktal tersebut dengan 8 pangkat 0, 1, 2, 3, dst sesuai dengan deretan angka oktal tersebut dari sebelah kanan. </p>
      <span class="font-weight-normal">Contoh:</span>
      <p>6271<sub>8</sub> = ... <sub>10</sub></p>
      <p>Adapun cara pengerjaannya adalah seperti berikut ini:</p>
      <p>
          1  x  8<sup>0</sup>  =  1<br>
          7  x  8<sup>1</sup>  =  56<br>
          2  x  8<sup>2</sup>  =  128<br>
          6  x  8<sup>3</sup>  =  3072   
      </p>
      <p>Lalu kita jumlahkan hasil kali tersebut, 3072 + 128 + 56 + 1 = 3257</p>
      <p>Jadi, 6271<sub>8</sub> = 3257<sub>10</sub></p> 
      </figcaption>
