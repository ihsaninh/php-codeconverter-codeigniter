      <h6>Catatan:</h6>
      <figcaption class="figure-caption font-weight-light">
      <p>Untuk mengkonversikan bilangan desimal yang berbasis 10 ke bilangan oktal yang berbasis 8, kita harus membagi bilangan desimal ke basis bilangan oktal yaitu 8, hasilnya kemudian dibulatkan kebawah dan sisa hasil pembagiannya dicatat dan disimpan. Sisa pembagiannya tersebut kemudian diurutkan dari yang paling akhir hingga yang paling awal.: </p>
      <span class="font-weight-normal">Contoh:</span>
      <p>70<sub>10</sub> = ... <sub>8</sub></p> 
      <p>
          70/8 = 8 sisa 6<br>
          8/8 = 1 sisa 0<br>
          1/8 = 0 sisa 1   
      </p>
      <p>Jadi, 70<sub>10</sub> = 106<sub>8</sub></p> 
      </figcaption>
