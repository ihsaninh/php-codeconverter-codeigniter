          <h6>Catatan:</h6>
          <figcaption class="figure-caption font-weight-light">
          <p>Kebutuhan untuk pengkodean Base64 berasal dari masalah yang melekat dalam mentransmisikan media dalam format biner raw ke sistem berbasis teks.</p>
          <p>Karena sistem berbasis teks (seperti email ) menafsirkan data biner sebagai berbagai karakter, termasuk karakter perintah khusus, banyak data biner yang akan dikirim ke media transfer akan disalahartikan oleh sistem-sistem tersebut dan hilang atau rusak dalam proses transmisi.</p>
          <p>Salah satu metode penyandian data biner semacam ini dengan cara yang menghindari masalah transmisi tersebut adalah dengan mengirimkannya sebagai teks ASCII biasa dalam format yang disandikan Base64. Ini adalah salah satu teknik yang digunakan oleh standar MIME untuk mengirim data selain teks biasa.</p> 
