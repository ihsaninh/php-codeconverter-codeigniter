      <h6>Catatan:</h6>
      <figcaption class="figure-caption font-weight-light">
      <p class="text-justify">Cara untuk mengkonversikan bilangan hexadesimal ke oktal yaitu dengan mengkonversikan bilangan hexadesimal tersebut ke biner terlebih dahulu kemudian bilangan biner tersebut dikonversikan ke bentuk oktal</p>
      <span class="font-weight-normal">Contoh:</span>
      <p>9F4<sub>16</sub> = ... <sub>8</sub></p>
       <ol class="mylist">
        <li>Kita pisahkan 9F4 menjadi  9, F dan 4 kemudian konversikan ke biner</li>
         <li>9  =  1001       F  = 15 = 1111        4  =  0100</li>
          <li>Setelah dapat biner nya yaitu 100111110100, kemudian konversi biner tersebut ke oktal.</li>
          <li>100 = 4 111 = 7 110 = 6 100 = 4</li>
        </ol>
      <p>Jadi, 9F4<sub>2</sub> = 4764<sub>8</sub></p> 
